/**
 * 获取所有的省份列表。
 *
 * @returns {Array}
 */
function get_provinces() {
  var provinces = [];
  for (var index in all_districts) {
    //console.debug('all_districts[' + index + ']: ' + all_districts[index]['N']);
    provinces.push(all_districts[index]['N']);
  }

  return provinces;
}

/**
 * 返回一个省所有的城市列表。
 *
 * @param province 省份名称
 */
function get_cities(province) {
  var cities = [];
  for (var index in all_districts) {
    // console.debug('all_districts[' + index + ']: ' + all_districts[index]['N']);
    var p = all_districts[index];
    if (p['N'] == province) {
      for (var index in p['S']) {
        cities.push(p['S'][index]['N']);
      }
      break;
    }
  }
  return cities;
}

/**
 * 返回一个城市所有的区/县列表。
 *
 * @param city
 */
function get_districts(province, city) {
  var districts = [];
  for (var p_index in all_districts) {
    var p = all_districts[p_index];
    if (p['N'] != province) {
      continue;
    }
    for (var c_index in p['S']) {
      var c = p['S'][c_index];
      if (c['N'] == city) {
        for (var d_index in c['S']) {
          var district_name = c['S'][d_index]['N'];
          if (district_name != '市辖区') {
            // 排除 '市辖区'
            districts.push(district_name);
          }
        }
        break;
      }
    }
    break;
  }

  return districts;
}

function fill_selector(selector, options) {
  selector.empty();
  for (var index in options) {
        // console.debug('append: ' + options[index]);
        selector.append('<option value="'+options[index]+'">' + options[index] + '</option>');
    }

  selector.trigger('change');
}

/**
 * 设置省/市/区的值，并设置相应的下级select能选的值
 * @param selector
 * @param val
 */
function set_selector_val(selector, val) {
  selector.val(val);
  selector.trigger('change');
}

function init_district_selector($province_selector, $city_selector, $district_selector,
    default_province, default_city, default_district) {
  $province_selector.change(function() {
      var province = $(this).val();
      fill_selector($city_selector, get_cities(province));
  });

  $city_selector.change(function() {
    var province = $province_selector.val();
      var city = $(this).val();
      fill_selector($district_selector, get_districts(province, city));
  });

  // 加载省的数据
  fill_selector($province_selector, get_provinces());

  // 设置缺省值
  if (default_province) {
    set_selector_val($province_selector, default_province);
  }

  if (default_city) {
    set_selector_val($city_selector, default_city);
  }

  if (default_district) {
    set_selector_val($district_selector, default_district);
  }
}

